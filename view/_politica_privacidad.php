        <h2>Politica de Privacidad</h2>
        <br>
        <p>Para demostrar nuestro compromiso con la privacidad de su información personal, hemos desarrollado este documento, que describe nuestras prácticas de privacidad de la información que recogemos en
            nuestro aplicativo en orientacionvocacional.guiame.org. Esta política le informa sobre:</p>

        <ul>
            <ol>1) ¿Qué información personal recogemos?</ol>
            <ol>2) ¿Quién recoge la información?</ol>
            <ol>3) ¿Cómo se utiliza su información personal o la revelamos a terceros?</ol>
            <ol>4) ¿Qué opciones tiene usted respecto a la recopilación, uso y distribución de su información
                personal?
            </ol>
            <ol>5) ¿Cómo corregir, actualizar o borrar su información personal?</ol>
            <ol>6) ¿Qué tipo de procedimientos de seguridad utilizamos para proteger la pérdida, mal uso o
                alteración de la información bajo nuestro control; y
            </ol>
            <ol>7) ¿Cómo puede ser modificado esta política?
            </ol>
        </ul>

        <p>"Información personal" es información que permita a
            alguien para identificar o contactar, incluyendo, por ejemplo, su nombre completo, dirección de correo electrónico. La
            información personal no incluye información agregada que, por sí sola, no permite la identificación de personas.</p>
        <br>


        <h4>(1) Qué información personal recogemos?</h4>
            <p>Los usuarios pueden navegar a través de la parte informativa de nuestro Sitio Web y aplicativo sin
            proporcionar ninguna información personal. Sin embargo, si decide convertirse en un usuario tendrá que
            registrarse e haciendo uso de su cuenta de Facebook. Si se inscribe, Facebook le pedirá su permiso para compartir cierta información de su cuenta de Facebook
            con OrientacionVocacional. Esto incluye su información básica (nombre, foto de perfil, género, ID de
            usuario, correo electrónico lista de amigos, y cualquier otra información que usted hizo pública).</p>

        <p>Esta información es recogida por Facebook y se proporciona a nosotros bajo los términos de la
            política de privacidad de Facebook. Se puede controlar la información que recibimos de Facebook usando la
            configuración de privacidad de su cuenta de Facebook.</p>        

                   <p>Si se registra su cuenta de OrientacionVocacional será conectada a su cuenta de
            Facebook y que tendrá la capacidad de compartir su actividad con Facebook y será publicado en
            paquetes de noticias a sus amigos de Facebook. Esto no se hace de forma automática y usted tiene la opción
            de compartir o no su actividad en Facebook.</p> 
        <br>


        <h4>(2) Quién recoge su información?</h4>
            <p>OrientacionVocacional posee y opera el Sitio Web y posee y mantiene la información personal obtenida a
            través del Sitio Web. Nuestra dirección de correo electrónico para los problemas de privacidad o preguntas
            es soporte@guiame.org
        </p>
        <br>

        <h4>(3) Cómo se utiliza su información personal o la revelamos a terceros?</h4>
            <p>Orientación Vocacional no hace proporciona ningún tipo de información a terceros, los datos recolectados
            meramente
            para fines estadisticos. </p>

                    <p>A pesar de que su privacidad es muy importante para nosotros, debido al entorno normativo y
            seguridad jurídica existente, no podemos asegurar plenamente que su información personal no será revelada a
            terceros adicionales. Por ejemplo, podemos ser forzados u obligados a revelar información al gobierno o a
            terceros bajo ciertas circunstancias, o terceros pueden interceptar ilegalmente o acceder a transmisiones o
            comunicaciones privadas. Además, en el improbable caso de que nosotros o una entidad gubernamental que
            investigar o resolver posibles problemas o preguntas, podemos (y usted nos autoriza a hacerlo) revelar
            cualquier información sobre usted a entidades privadas, la policía u otras autoridades gubernamentales como
            que, en nuestra única discreción, creemos necesario o apropiado.
        </p>
        <br>

        <h4>(4) Qué opciones tiene usted respecto a la recopilación, uso y distribución de su información
            personal?</h4>
        <p>Si no desea que utilicemos sus datos personales para los fines establecidos en esta política o la
            finalidad establecida en el momento de la presentación de dicha información, por favor no convertirse en un
            usuario o enviar información personal a nosotros.
        </p>
        <br>
        <h4>(5) Cómo corregir, actualizar o borrar su información personal?</h4>
        <p>Puede acceder y actualizar su información personal poniéndose en contacto con Orientación Vocacional en
            soporte@ticademia.org o también podrá hacerlo mediante las funciones disponibles a través del aplicativo.

        </p>
        <br>

        <h4>(6) Qué tipo de procedimientos de seguridad utilizamos para proteger la pérdida, mal uso o
            alteración de la información bajo nuestro control</h4>
        <p>Hemos establecido y mantenemos procedimientos de seguridad razonables para proteger la
            confidencialidad, seguridad e integridad de su información personal. Recopilamos información personal sólo
            de nuestros usuarios. Implementamos importantes esfuerzos fuera de línea para proteger aún
            más esta información. Sin embargo,
            por favor, reconozca que la "seguridad perfecta" no existe en Internet, y Orientación Vocacional no puede
            garantizar y no
            garantiza la seguridad absoluta de su información personal.
        </p>
        <br>

        <h4>(7)Cómo puede ser modificado esta política?</h4>
        <p>Orientación Vocacional podrá modificar o cambiar esta política en cualquier momento. Publicaremos cualquier
            revisión de esta política en el Sitio Web, por lo que los usuarios son conscientes de estos cambios.
            Cualquier usuario que facilite información personal
            adicional después de que estos cambios sean publicados, se considerará que han aceptado la política de
            privacidad revisada. Si en algún momento decidimos utilizar la información personal de una manera diferente
            a la establecida en el momento de su recogida, vamos a enviar a dicha persona un correo electrónico
            notificando él / ella de tales cambios y dar él / ella la opción en cuanto a si se debe permitir nos permite
            utilizar su información de esta manera diferente. Sin tal consentimiento, utilizaremos la información
            personal de acuerdo con la política de privacidad bajo la cual fueron recogidos.

                    
        <p>Esperamos que esta política aclara nuestros procedimientos con respecto a su información personal.
            Si usted tiene alguna pregunta, puede ponerse en contacto con nosotros en soporte@ticademia.org</p>
        </p>
